{
    this->pin = _pin;
}

/*********************************************************************************************************
 ** Function name:           ...
 ** Descriptions:            ...
 *********************************************************************************************************/
void Button::Pulse()
{
    unsigned long now = millis();
    if(this->nextMillis < now)
    {
        if(this->state)
        {
            digitalWrite(this->pin, HIGH);
            this->nextMillis = now + this->duration;
            this->state = false;
        }
        else
        {
            digitalWrite(this->pin, LOW);
            this->nextMillis = now + this->gap;
            this->state = true;
        }
    }
}

